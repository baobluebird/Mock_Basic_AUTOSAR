#ifndef STD_TYPES_H
#define STD_TYPES_H

#include <stdint.h> 

/** @brief Define AUTOSAR data type */
typedef uint8_t uint8; /**< 8-bit unsigned */
typedef uint16_t uint16; /**< 16-bit unsigned */
typedef uint32_t uint32; /**< 32-bit unsigned */
typedef int8_t sint8; /**< 8-bit signed */
typedef int16_t sint16; /**< 16-bit signed */
typedef int32_t sint32; /**< 32-bit signed */

#endif